class Solution:
  def reverseBits(self, n: int) -> int:
    return (
      ((n >>  0 & 1) << 31) |
      ((n >>  1 & 1) << 30) |
      ((n >>  2 & 1) << 29) |
      ((n >>  3 & 1) << 28) |
      ((n >>  4 & 1) << 27) |
      ((n >>  5 & 1) << 26) |
      ((n >>  6 & 1) << 25) |
      ((n >>  7 & 1) << 24) |
      ((n >>  8 & 1) << 23) |
      ((n >>  9 & 1) << 22) |
      ((n >> 10 & 1) << 21) |
      ((n >> 11 & 1) << 20) |
      ((n >> 12 & 1) << 19) |
      ((n >> 13 & 1) << 18) |
      ((n >> 14 & 1) << 17) |
      ((n >> 15 & 1) << 16) |
      ((n >> 16 & 1) << 15) |
      ((n >> 17 & 1) << 14) |
      ((n >> 18 & 1) << 13) |
      ((n >> 19 & 1) << 12) |
      ((n >> 20 & 1) << 11) |
      ((n >> 21 & 1) << 10) |
      ((n >> 22 & 1) <<  9) |
      ((n >> 23 & 1) <<  8) |
      ((n >> 24 & 1) <<  7) |
      ((n >> 25 & 1) <<  6) |
      ((n >> 26 & 1) <<  5) |
      ((n >> 27 & 1) <<  4) |
      ((n >> 28 & 1) <<  3) |
      ((n >> 29 & 1) <<  2) |
      ((n >> 30 & 1) <<  1) |
      ((n >> 31 & 1) <<  0)
    )
